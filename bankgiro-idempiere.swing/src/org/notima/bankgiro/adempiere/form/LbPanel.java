    public void initPanel(XX_LBSettingsDialog parent, SortedMap<String, MLBSettings> settings) {
    	m_parentDialog = parent;
    	m_settings = settings;
    	m_bankFileFormats = new Vector<RefListWrapper>();
    	
    	// Read bank list formats
    	X_AD_Reference utlBankFileFormat = new Query(Env.getCtx(), X_AD_Reference.Table_Name, X_AD_Reference.COLUMNNAME_Name + "=?", null)
    						.setParameters(new Object[]{"utl_bankfileformat"})
    						.first();
    	
    	if (utlBankFileFormat!=null) {
    		List<X_AD_Ref_List> items = new Query(Env.getCtx(), X_AD_Ref_List.Table_Name, "AD_Reference_ID=?", null)
    						.setParameters(new Object[]{utlBankFileFormat.get_ID()})
    						.list();
    		for (X_AD_Ref_List i : items) {
    			m_bankFileFormats.add(new RefListWrapper(i));
    		}
    	}
    	ComboBoxModel model = new DefaultComboBoxModel(m_bankFileFormats);
    	utlFormatCombo.setModel(model);
    	
    }
    
    @Override
    public void getForm() {
        MLBSettings.setSetting(MLBSettings.LB_OUTPUT_DIR, directoryText.getText());
        MLBSettings.setSetting(MLBSettings.LB_FILE_NAME, fileNameText.getText());
        MLBSettings.setSetting(MLBSettings.LB_ADD_TS, addTimeToFileCheck.isSelected() ? "Y" : "N");
        MLBSettings.setSetting(MLBSettings.LB_FILE_SUFFIX, fileSuffixText.getText());
        MLBSettings.setSetting(MLBSettings.LB_RECONCILIATION_DIR, defaultDirText.getText());
        MLBSettings.setSetting(MLBSettings.LB_RECONCILIATION_MOVETO_DIR, archiveDirText.getText());
        RefListWrapper item = (RefListWrapper)utlFormatCombo.getSelectedItem();
        String utlFmt = item!=null ? item.getValue() : "";
        MLBSettings.setSetting(MLBSettings.LB_UTL_FMT, utlFmt);
    }

    @Override
    public void setForm() {

        MLBSettings outputDir = m_settings.get(MLBSettings.LB_OUTPUT_DIR);
        directoryText.setText(outputDir!=null ? outputDir.getName() : "");
        MLBSettings fileName = m_settings.get(MLBSettings.LB_FILE_NAME);
        fileNameText.setText(fileName!=null ? fileName.getName() : "BGDATA");
        MLBSettings addTime = m_settings.get(MLBSettings.LB_ADD_TS);
        addTimeToFileCheck.setSelected(addTime!=null && "Y".equalsIgnoreCase(addTime.getName()));
        MLBSettings fileSuffix = m_settings.get(MLBSettings.LB_FILE_SUFFIX);
        fileSuffixText.setText(fileSuffix!=null ? fileSuffix.getName() : "IN");

        MLBSettings defaultDir = m_settings.get(MLBSettings.LB_RECONCILIATION_DIR);
        defaultDirText.setText(defaultDir!=null ? defaultDir.getName() : "");
        MLBSettings archiveDir = m_settings.get(MLBSettings.LB_RECONCILIATION_MOVETO_DIR);
        archiveDirText.setText(archiveDir!=null ? archiveDir.getName() : "");
        MLBSettings utlFmt = m_settings.get(MLBSettings.LB_UTL_FMT);
        RefListWrapper item = null;
        for (int i=0; utlFmt!=null && i<m_bankFileFormats.size(); i++) {
        	if (m_bankFileFormats.get(i).getValue().equals(utlFmt.getName())) {
        		item = m_bankFileFormats.get(i);
        		break;
        	}
        }
        utlFormatCombo.setSelectedItem(item);

    }



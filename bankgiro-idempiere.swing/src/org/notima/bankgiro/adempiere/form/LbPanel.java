    /**
     * Wrap the RefList class to make it work nicely in a combo box
     * 
     * @author daniel.tamm
     *
     */
    private class RefListWrapper {
    	public I_AD_Ref_List elem;
    	
    	public RefListWrapper(I_AD_Ref_List e) {
    		elem = e;
    	}
    	
    	public String toString() {
    		return elem.getName();
    	}
    	
    	public String getValue() {
    		return elem.getValue();
    	}
    }
    
